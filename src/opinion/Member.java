package opinion;

public class Member {

    private String name;
    private int date;
    private String login;
    private String pwd;
    private String mail;

    public Member(String name, int date, String login, String pwd, String mail) {
        this.name = name;
        this.date = date;
        this.login = login;
        this.pwd = pwd;
        this.mail = mail;
    }
}
